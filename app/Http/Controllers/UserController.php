    /**
     * Display the specified resource.
     *
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $user = User::where('id', $id)->with(['companies', 'posts' => function($post){
            $post->with(['products' => function($product){
                $product->with('images');
            },
            'users' => function($user){
                $user->with('companies');
            }]);
        }])->first();
        return json_encode($user);
    }

